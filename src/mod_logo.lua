	lspr(g_logo_data.amrg, 47, 60+1, 34) -- 34 is the width of the logo
	clip(0, 0, 127, 127)
	lspr(g_logo_data.o,60, 60+l_o, 8)
end

-- embed this function in your game
-- draws an uncompressed image.
function lspr(arr, x, y, w, logo)
	for i, c in pairs(arr) do
		if c != 16 then pset(x,y,c == 7 and y >= 66 and 5 or c) end
		if i % w == 0 then x -= w y += 1 end
		x+=1
	end
end
